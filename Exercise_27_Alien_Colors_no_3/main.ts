//First version when alien color is green
let alien_color = "green";

if (alien_color == "green") {
  console.log(`\n"Yes color is ${alien_color}, player earned 5 points"`);
} else if (alien_color == "yellow") {
  console.log(`\n"Yes color is ${alien_color}, player earned 10 points"`);
} else if (alien_color == "red") {
  console.log(`\n"Yes color is ${alien_color}, player earned 15 points"`);
} else {
  console.log(
    "\nAlien color is neither green or yellow or red, player didnt earn any point"
  );
}

